public class StringPrimitiveConcat{
    public static void main(String args[]) {
        int number = 24;
        System.out.println("This String is concatenated with an int at the end: " + number);
    
        double d = 3.1405;
        System.out.println("This String is concatenate with a double at the end: " + d);
        
        char letter = 't';
        System.out.println("This String is concatenated with a char at the end: " + letter);

        boolean b = true;
        System.out.println("This String is concatenated with a boolean at the end: " + b);
    }
}
